
#ifndef BIBLIOTECAPASAGES_H_
#define BIBLIOTECAPASAGES_H_
#include <stdio.h>
#include <stdlib.h>

/// @brief Hace el pasage de Fahrenheit a Celsius, tomando el grado fahrenheit anteriormente validado.
/// @param El entero correspondiente a grados Fahrenheit ya validado.
/// @return Devuelve el resultado del pasage, es decir devuelve (°C).
int PasageACelcius(int);

/// @brief Hace el pasage de Celsius a Fahrenheit, tomando el grado Celsius anteriormente validado.
/// @param El entero correspondiente a grados Celsius ya validado.
/// @return Devuelve el resultado del pasage, es decir devuelve (°F).
int PasageAFahrenheit(int);
#endif /* BIBLIOTECAPASAGES_H_ */
